vi.stubEnv('VITE_OPENCODE_API_KEY', 'test-key');
vi.stubEnv('VITE_GEMINI_API_KEY', 'test-key');
vi.stubEnv('VITE_SUPABASE_URL', 'https://test.supabase.co');
